using namespace std;
const int maxn=5e3+10;
int n,a[maxn],dp[maxn][maxn],res;
int main(){
	scanf("%d",&n);
	for(int i=1;i<=n;i++)scanf("%d",&a[i]);
	sort(a+1,a+1+n);
	for(int i=2;i<=n;i++){
		for(int j=1;j<i;j++){
			int pos=lower_bound(a+1,a+1+n,2*a[j]-a[i])-a;
			if(a[pos]+a[i]==2*a[j])	dp[i][j]=max(dp[i][j],dp[j][pos]+1);
			res=max(res,dp[i][j]);
		}
	}
	printf("%d",res+2);
	return 0;
}